						<p style="    line-height: 1.5em;">Adopt an Athlete is an iA Foundation initiative designed to give deserving athletes, from various endurance sports, access to the right resources to improve their performance and compete in relevant competitions, thus achieve their potential athletically. 42.1 has partnered with iA Foundation to manage this program. Through this association, 42.1 looks to mentor budding athletes and help them train better. Resources are currently limited to those pursuing athletics or multisport, such as running, cycling, swimming, triathlon, tennis, and cricket. However, other endurance sports will be considered.</p>

						<p class="txt-clr"><strong>Resources available to Athletes under the program, include but not limited to:</strong></p>

						<ul>
							<li>Membership to the 42.1 community, and hence access to its mentors, individual and institutional partners (training and other academies)</li>
							<li>Guidance on effective nutrition and diet plans</li>
							<li>Ongoing Mentorship with endurance sport experts. This includes multi faculty guidance, training tips, workshops, individual training sessions, and more</li>
							<li>Access to subsidized schooling and education</li>
							<li>Guidance and possible support for training and competition gear</li>
							<li>Subsidized access to partner training academies</li>
							<li>Support for Game/Race/Sport Registrations</li>
						</ul>

						<p class="txt-clr"><strong>Application & Selection Process:</strong></p>
						<ul>
							<li>The candidate (or a good Samaritan on behalf of someone they think to be deserving) should fill the application form, and supporting documents</li>
							<li>The 42.1 and iA Foundation committees will review each application, taking into account advise and recommendations from the 42.1 mentor panel</li>
							<li>The panel will shortlist candidates and meet with them for a final review</li>
							<li>The panel may ask for relevant references (and speak with guardians or associates of the candidate) to ensure transparency and relevance</li>
							<li>Announcement and On-Boarding</li>
						</ul>

						<p class="txt-clr"><strong>Candidate Criteria for selection:</strong></p>
						<ul>
							<li>All applicants must be citizens of India</li>
							<li>All applicants must be residents of Ahmedabad (the program may be extended to other cities later)</li>
							<li>Excellent performance and potential in any competitive endurance sport</li>
							<li>All applicants must be from a low-income strata, and willing to provide financial statements and/or proof for the same</li>
							<li>Applications should be between the age of 8 and 20</li>
							<li>Applicants must demonstrate a strong commitment to pursue their sport and continue to show reasonable academic/school performance. Those who cannot afford to attend school will be eligible to subsidized schooling at a partner institution provided they show commitment to attend.</li>
						</ul>



