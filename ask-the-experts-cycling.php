						<p><b>Yagnesh Ahir,</b> Mentor-on-Board at 42.1, a Professional Cyclist himself and a founder of Ahmedabad’s first ever Cycling Racing team ‘Amdavad Crankmeisters’ gave valuable insights on cycling for amateurs to experts and tried clearing their doubts.</p>

						<p>Here is an excerpt;<p>
						<p><b>Q: Things to keep in mind while buying or renting a cycle?</b><p>

						<p><b>YA:</b> There are few things, one should keep in mind while buying or renting a cycle;<p>
						<ul>
							<li>A cycle should go well with one’s body posture and, a test to that could be;</li>
							<li>A bike should give stretch at the back of a knee while riding</li>
						</ul>

						<p><b>Q: For a beginner; things he/she should invest?</b><p>

						<p><b>YA:</b> A small investment in the following can help athletes in increasing their efficiency and productivity, significantly.<p>
						<ul>
							<li><b>Right Cycle</b> - Could go a long way in saving money and time spent on Orthopedic or Physio’s clinics</li>
							<li><b>Apparel</b> - A good pair of padded shorts will help prevent saddle sores</li>
							<li><b>Cleats (with Right Cycling Shoes)</b> - For maximum energy transformation and improved performance</li>
							<li><b>Security Gadgets</b> - Right helmet, a light on the helmet (for night riding) go a long way in ensuring athlete’s safety</li>
							
						</ul>	

						<p><b>Q: Is it advisable to use the regular running/walking shoes while cycling?</b><p>

						<p><b>YA:</b> When paddling with regular shoes, there is no attachment between the paddle and shoes, so, one would end up using more energy in pulling, that leads to inefficient use of power. If a rider, speeds up with regular shoes, he might slip off. Therefore, it is advisable that cyclists do not use their regular shoes while cycling.<p>
						
						<p><b>Q: Importance of ‘Cleats’ while riding the cycles and how one should begin using them?</b><p>

						<p><b>YA:</b> Cleats are nothing but sockets that can be attached to bike’s paddles. Cycling with cleats can be difficult in the beginning; however, it gets more comfortable with more rides. An athlete would require a technique to get it off!
						
						<p>One should always try to unclip with their dominant leg (For a right-handed person, their right leg is their dominant leg) Begin with taking a ride to a nearby garden. Find a railing, hold on to it and try to clip and unclip (A softer surface in case of a fall).</p>
