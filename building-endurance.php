		function openAllCarousel()
		{
			
			var carouselone = document.getElementById("collapseOne").classList;
			var carouseltwo = document.getElementById("collapseTwo").classList;
			var carouselthree = document.getElementById("collapseThree").classList;
			var carouselfour = document.getElementById("collapseFour").classList;
			
			if(carouselone.contains("in") && carouseltwo.contains("in") && carouselthree.contains("in") && carouselfour.contains("in"))
			{ 
				document.getElementById("p-open-all").innerHTML="Open All";			
				carouselone.remove("in");
				carouselone.remove("show");
				carouseltwo.remove("in");
				carouseltwo.remove("show");
				carouselthree.remove("in");
				carouselthree.remove("show");
				carouselfour.remove("in");
				carouselfour.remove("show");			
			}
			else
			{
				document.getElementById("p-open-all").innerHTML="Close All";
				carouselone.add("in");
				carouseltwo.add("in");
				carouselthree.add("in");
				carouselfour.add("in");
				//document.getElementById("performance-a").remove("collapsed");	
			}
		}
	</script>
